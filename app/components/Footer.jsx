import {Suspense} from 'react';
import {Await, NavLink} from '@remix-run/react';
import {FaFacebook, FaInstagram, FaTiktok} from 'react-icons/fa'; // Import icons from react-icons
import logo from 'app/assets/logo.svg';

/**
 * @param {FooterProps}
 */
export function Footer({footer: footerPromise, header, publicStoreDomain}) {
  return (
    <Suspense>
      <Await resolve={footerPromise}>
        {(footer) => (
          <div className="flex flex-col items-center">
            <img alt="meow" src={logo} className="w-16 h-16 mb-4" />
            <footer className="footer w-full">
              {footer?.menu && header.shop.primaryDomain?.url && (
                <FooterMenu
                  menu={footer.menu}
                  primaryDomainUrl={header.shop.primaryDomain.url}
                  publicStoreDomain={publicStoreDomain}
                />
              )}
              <div className="flex justify-center space-x-4 mt-4">
                <a
                  href="https://facebook.com"
                  className="text-current hover:opacity-75"
                  target="_blank"
                  rel="noopener noreferrer"
                >
                  <FaFacebook size={24} color="white" />
                </a>
                <a
                  href="https://instagram.com"
                  className="text-current hover:opacity-75"
                  target="_blank"
                  rel="noopener noreferrer"
                >
                  <FaInstagram size={24} color="white" />
                </a>
                <a
                  href="https://tiktok.com"
                  className="text-current hover:opacity-75"
                  target="_blank"
                  rel="noopener noreferrer"
                >
                  <FaTiktok size={24} color="white" />
                </a>
              </div>
            </footer>
          </div>
        )}
      </Await>
    </Suspense>
  );
}

/**
 * @param {{
 *   menu: FooterQuery['menu'];
 *   primaryDomainUrl: FooterProps['header']['shop']['primaryDomain']['url'];
 *   publicStoreDomain: string;
 * }}
 */
function FooterMenu({menu, primaryDomainUrl, publicStoreDomain}) {
  return (
    <nav
      className="footer-menu flex justify-center flex-wrap"
      role="navigation"
    >
      {(menu || FALLBACK_FOOTER_MENU).items.map((item) => {
        if (!item.url) return null;
        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain) ||
          item.url.includes(primaryDomainUrl)
            ? new URL(item.url).pathname
            : item.url;
        const isExternal = !url.startsWith('/');
        return isExternal ? (
          <a
            href={url}
            key={item.id}
            rel="noopener noreferrer"
            target="_blank"
            className="mx-2"
          >
            {item.title}
          </a>
        ) : (
          <NavLink
            end
            key={item.id}
            prefetch="intent"
            style={activeLinkStyle}
            to={url}
            className="mx-2"
          >
            {item.title}
          </NavLink>
        );
      })}
    </nav>
  );
}

